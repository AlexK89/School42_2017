#include <stdio.h>

int ft_sqrt(int nb)
{
    int i;
    
    i = 0;
    while (i < nb / 2 && nb < 1000)
    {
        if (i * i == nb)
            return (i);
        i++;
    }
    while (i < nb / 20 && nb > 1000)
    {
        if (i * i == nb)
            return (i);
        i++;
    }
    return(0);
}

int main(void)
{
    printf("%i\n", ft_sqrt(2147395600));
}
